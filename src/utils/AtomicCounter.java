package utils;
import java.util.concurrent.atomic.AtomicInteger;

/**
 *
 * @author anantoni
 */

public class AtomicCounter {
    private final AtomicInteger c = new AtomicInteger(0);

    public void increment() {
        c.incrementAndGet();
    }

    public void decrement() {
        c.decrementAndGet();
    }

    public int value() {
        return c.get();
    }

}
